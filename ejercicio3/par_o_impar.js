/**
 * Verifica si un número es par o impar.
 * @param {number} numero - El número a verificar.
 */
function verificarParImpar(numero) {
    if (numero % 2 === 0) {
        console.log("El número es par.");
    } else {
        console.log("El número es impar.");
    }
}

verificarParImpar(4);  
verificarParImpar(7);  
